/**
 * Build-time: parse Paratext-style `usfm.sty` and emit `packages/usfm-editor/usfm-markers.css`.
 * Run from repo root: `node scripts/generate-marker-css.mjs`
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const styPath = path.join(root, 'packages/usfm-editor/data/usfm.sty');
const outPath = path.join(root, 'packages/usfm-editor/usfm-markers.css');

const src = fs.readFileSync(styPath, 'utf8');
const lines = src.split(/\r?\n/);

/** @type {{ name: string, styleType: string, props: Record<string, string> }[]} */
const markers = [];
let cur = null;

for (const raw of lines) {
  const line = raw.trim();
  if (!line || line.startsWith('#')) continue;
  if (line.startsWith('\\Marker ')) {
    if (cur && cur.name) markers.push(cur);
    const name = line.slice('\\Marker '.length).trim();
    cur = { name, styleType: 'Paragraph', props: {} };
    continue;
  }
  if (!cur) continue;
  const m = line.match(/^\\([A-Za-z]+)\s*(.*)$/);
  if (!m) continue;
  const key = m[1];
  const val = (m[2] ?? '').split('#')[0].trim();
  if (key === 'StyleType') {
    cur.styleType = val;
  } else if (
    ['FontSize', 'Bold', 'Italic', 'Justification', 'LeftMargin', 'RightMargin', 'FirstLineIndent', 'SpaceBefore', 'SpaceAfter', 'Superscript', 'Subscript', 'Underline', 'Smallcaps'].includes(
      key
    )
  ) {
    cur.props[key] = val === '' ? 'true' : val;
  }
}

if (cur && cur.name) markers.push(cur);

function escCss(s) {
  return String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

function fontSizeToCss(pt) {
  const n = parseFloat(pt);
  if (!Number.isFinite(n)) return null;
  return `font-size: calc(${n} / 12 * 1rem)`;
}

function numToRem(n, factor = 2) {
  const x = parseFloat(n);
  if (!Number.isFinite(x)) return null;
  return `calc(${x} * ${factor} * 1rem)`;
}

function spaceToMargin(n) {
  const x = parseFloat(n);
  if (!Number.isFinite(x)) return null;
  return `calc(${x} * 0.125rem)`;
}

function rulesForMarker(m) {
  const decl = [];
  const p = m.props;

  if (p.FontSize) {
    const fs = fontSizeToCss(p.FontSize);
    if (fs) decl.push(fs);
  }
  if (p.Bold === 'true') decl.push('font-weight: 700');
  if (p.Italic === 'true') decl.push('font-style: italic');
  if (p.Underline === 'true') decl.push('text-decoration: underline');
  if (p.Smallcaps === 'true') decl.push('font-variant: small-caps');
  if (p.Superscript === 'true') {
    decl.push('vertical-align: super');
    decl.push('font-size: 0.75em');
  }
  if (p.Subscript === 'true') {
    decl.push('vertical-align: sub');
    decl.push('font-size: 0.75em');
  }

  const j = p.Justification;
  if (j === 'Center') decl.push('text-align: center');
  else if (j === 'Right') decl.push('text-align: right');
  else if (j === 'Left') decl.push('text-align: left');

  if (p.LeftMargin) {
    const lm = numToRem(p.LeftMargin, 2);
    if (lm) decl.push(`padding-left: ${lm}`);
  }
  if (p.RightMargin) {
    const rm = numToRem(p.RightMargin, 2);
    if (rm) decl.push(`padding-right: ${rm}`);
  }
  if (p.FirstLineIndent) {
    const fi = numToRem(p.FirstLineIndent, 2);
    if (fi) decl.push(`text-indent: ${fi}`);
  }
  if (p.SpaceBefore) {
    const sb = spaceToMargin(p.SpaceBefore);
    if (sb) decl.push(`margin-top: ${sb}`);
  }
  if (p.SpaceAfter) {
    const sa = spaceToMargin(p.SpaceAfter);
    if (sa) decl.push(`margin-bottom: ${sa}`);
  }

  return decl;
}

const out = [];
out.push('/* Generated by scripts/generate-marker-css.mjs — do not edit by hand */');
out.push('/* Source: packages/usfm-editor/data/usfm.sty */');
out.push('');

for (const m of markers) {
  const decl = rulesForMarker(m);
  if (decl.length === 0) continue;
  const body = decl.map((d) => `  ${d};`).join('\n');
  const name = escCss(m.name);
  const st = m.styleType.toLowerCase();
  if (st === 'paragraph' || st === 'para') {
    out.push(`.ProseMirror .usfm-para[data-marker='${name}'] {`);
    out.push(body);
    out.push('}');
    out.push('');
  } else if (st === 'character' || st === 'char') {
    out.push(`.ProseMirror .usfm-char[data-char-marker='${name}'] {`);
    out.push(body);
    out.push('}');
    out.push('');
  }
}

const text = out.join('\n') + '\n';
fs.writeFileSync(outPath, text, 'utf8');
console.log('Wrote', path.relative(root, outPath), `(${text.split(/\r?\n/).length} lines)`);
